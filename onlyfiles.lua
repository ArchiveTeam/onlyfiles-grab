processed = function(url)
  if downloaded[url] or addedtolist[url] then
    return true
  end
  return false
end

discover_item = function(target, item)
  if not target[item] then
--print('queuing' , item)
    target[item] = true
  end
end

find_item = function(url)
  local value = string.match(url, "^http?s://onlyfiles%.io/f/([0-9a-z]+)$")
  local type_ = "f"
  if value then
    item_type = type_
    item_value = value
    item_name_new = item_type .. ":" .. item_value
    if item_name_new ~= item_name then
      ids = {}
      ids[value] = true
      abortgrab = false
      tries = 0
      item_name = item_name_new
      print("Archiving item " .. item_name)
    end
  end
end

allowed = function(url, parenturl)
  if ids[url] then
    return true
  end

  for s in string.gmatch(url, "([0-9a-z]+)") do
    if ids[s] then
      return true
    end
  end

  return false
end

